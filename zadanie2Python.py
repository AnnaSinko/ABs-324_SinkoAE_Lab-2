# Функция для проверки, есть ли в числе нечетное количество цифр
def has_odd_digits(num):
    return len(str(num)) % 2 != 0

def main():
    n = int(input("Введите количество элементов: "))  # Читаем количество чисел

    count = 0  # Инициализируем счётчик

    for _ in range(n):
        num = int(input("Введите число: "))  # Читаем число
        if has_odd_digits(num):
            count += 1  # Увеличиваем счётчик, если количество цифр в числе нечетное

    print(count)  # Выводим количество чисел с нечетным количеством цифр

if __name__ == "__main__":
    main()